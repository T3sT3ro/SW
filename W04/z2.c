int main() {

    // ustaw tryb licznika
    // COM1A = 10   -- non-inverting mode
    // WGM1  = 1110 -- fast PWM top=ICR1
    // CS1   = 10  -- prescaler 8
    // ICR1  = 15624 -- próg resetu
    ICR1 = 256;  // aka cntr limit
    TCCR1A = _BV(COM1A1) | _BV(WGM11);
    TCCR1B = _BV(WGM12) | _BV(WGM13) | _BV(CS11);
    // ustaw pin OC1A (PD6) jako wyjście
    DDRB |= _BV(PB1);

    ADMUX = _BV(REFS0);                              // Vref on AVCC
    ADCSRA |= _BV(ADPS0) | _BV(ADPS1) | _BV(ADPS2);  // prescaler = 1/128
    ADCSRA |= _BV(ADEN);                             // enable ADC
    _delay_ms(2);

    while (1) {
        ADCSRA |= _BV(ADSC);
        while (!(ADCSRA & _BV(ADIF)))
            ;
        ADCSRA |= _BV(ADIF);
        // próg porównania
        // 255-ciemno = opór 0
        // 0-jasno = opór >700
        OCR1A = _ledTable[255-((ADC > 1023 ? 1023 : ADC)/4)];
        _delay_ms(2);
    }
}
